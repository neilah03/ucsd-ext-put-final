# counter.py

def inc(x):
    """
    Increments the value of x
  
    >>> inc(4)
    5
  
    """
    return x + 1
  
def dec(x):
    """
    Decrements the value of x
    
    >>> dec(5)
    4
    
    """
    return x - 1
    
def prod(n1, n2):
    """
    Multiplies the 2 values provided
    
    >>> prod(5, 9)
    45
    
    """
    return n1 * n2

def square(x):
    """
    Squares the value of x
    
    >>> square(9)
    81
    
    """
    return x ** 2

def div(n1, n2):
    """
    Divides n1 by n2
    
    >>> div(9, 3)
    3.0
    
    """
    return n1 / n2


    
